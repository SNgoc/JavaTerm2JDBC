public class Tim_so_chan_le {
    
    public static void OddEven(int num){
        if (num % 2 == 0) {
            System.out.println(num + " is a even number");
        }
        if (num % 2 != 0) {
            System.out.println(num + " is a odd number");
        }
    } 
}


